    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String id = jTextField1.getText();
        String nom1 = jTextField2.getText();
        String nom2 = jTextField3.getText();
        String ape1 = jTextField4.getText();
        String ape2 = jTextField5.getText();
        String correo = jTextField6.getText();
        String tel = jTextField7.getText();

        if (!id.equals("") && !nom1.equals("") && !ape1.equals("")) {
            if (!nom2.equals("") && !ape2.equals("")) {
                Cliente objc = new Cliente(id, nom1, nom2, ape1, ape2, correo, tel);
                ControllerCliente objcc = new ControllerCliente();
                boolean t = objcc.modificarCliente(objc);

                if (t) {
                    JOptionPane.showMessageDialog(null, "Se modifico el cliente");
                } else {
                    JOptionPane.showMessageDialog(null, "No se modifico el cliente");
                }
            } else {
                if (!correo.equals("") && !tel.equals("")) {
                    Cliente objc = new Cliente(id, nom1, ape1, correo, tel);
                    ControllerCliente objcc = new ControllerCliente();
                    boolean t = objcc.modificarCliente(objc);

                    if (t) {
                        JOptionPane.showMessageDialog(null, "Se modifico el cliente");
                    } else {
                        JOptionPane.showMessageDialog(null, "No se modifico el cliente");
                    }
                } else {
                    //  JOptionPane.showMessageDialog(null, "en el del tel y correo");
                    Cliente objc = new Cliente(id, nom1, ape1);
                    ControllerCliente objcc = new ControllerCliente();
                    boolean t = objcc.modificarCliente(objc);

                    if (t) {
                        JOptionPane.showMessageDialog(null, "Se modifico el cliente");
                    } else {
                        JOptionPane.showMessageDialog(null, "No se modifico el cliente");
                    }
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Por favor, rellenar los campos con asteriscos");
        }
        
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        jTextField5.setText("");
        jTextField6.setText("");
        jTextField7.setText("");
        jTextField8.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        String idc = jTextField8.getText();
        ControllerCliente objcc = new ControllerCliente();
        Cliente objc = objcc.consultarCliente(idc);

        if (objc != null) {
            jTextField1.setText(objc.getIdcliente());
            jTextField2.setText(objc.getNombre1cliente());
            jTextField2.setEnabled(true);
            if (objc.getNombre2cliente() != null) {
                jTextField3.setText(objc.getNombre2cliente());
                jTextField3.setEnabled(true);
            } else {
                jTextField3.setEnabled(true);
            }
            jTextField4.setText(objc.getApellido1cliente());
            jTextField4.setEnabled(true);
            if (objc.getApellido2cliente() != null) {
                jTextField5.setText(objc.getApellido2cliente());
                jTextField5.setEnabled(true);
            } else {
                jTextField5.setEnabled(true);
            }
            if (objc.getCorreoelectronico() != null) {
                jTextField6.setText(objc.getCorreoelectronico());
                jTextField6.setEnabled(true);
            } else {
                jTextField6.setEnabled(true);
            }
            if (objc.getTelefonocliente() != null) {
                jTextField7.setText(objc.getTelefonocliente());
                jTextField7.setEnabled(true);
            } else {
                jTextField7.setEnabled(true);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Cliente no encontrado");
        }

    }//GEN-LAST:event_jButton3ActionPerformed

