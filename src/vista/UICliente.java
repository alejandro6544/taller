    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String id = jTextField1.getText();
        String nom1 = jTextField2.getText();
        String nom2 = jTextField3.getText();
        String ape1 = jTextField4.getText();
        String ape2 = jTextField5.getText();
        String correo = jTextField6.getText();
        String tel = jTextField7.getText();
        
        if (!id.equals("") && !nom1.equals("") && !ape1.equals("")) {
            if (!nom2.equals("") && !ape2.equals("")) {
                Cliente objc = new Cliente(id, nom1, nom2, ape1, ape2, correo, tel);
                ControllerCliente objcc = new ControllerCliente();
                boolean t = objcc.insertCliente(objc);

                if (t) {
                    JOptionPane.showMessageDialog(null, "Se creo el cliente");
                } else {
                    JOptionPane.showMessageDialog(null, "No se creo el cliente");
                }
            } else {
                if (!correo.equals("") && !tel.equals("")) {
                    Cliente objc = new Cliente(id, nom1, ape1, correo, tel);
                    ControllerCliente objcc = new ControllerCliente();
                    boolean t = objcc.insertCliente(objc);

                    if (t) {
                        JOptionPane.showMessageDialog(null, "Se creo el cliente");
                    } else {
                        JOptionPane.showMessageDialog(null, "No se creo el cliente");
                    }
                } else {
//                    JOptionPane.showMessageDialog(null, "en el del tel y correo");
                    Cliente objc = new Cliente(id, nom1, ape1);
                    ControllerCliente objcc = new ControllerCliente();
                    boolean t = objcc.insertCliente(objc);

                    if (t) {
                        JOptionPane.showMessageDialog(null, "Se creo el cliente");
                    } else {
                        JOptionPane.showMessageDialog(null, "No se creo el cliente");
                    }
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Por favor, rellenar los campos con asteriscos");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

